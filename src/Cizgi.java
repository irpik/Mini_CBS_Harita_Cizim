
public class Cizgi {
  private int[] x;
  private int[] y;
  private int kalinlik;

    public Cizgi(int[] x, int[] y, int kalinlik) {
        this.x = x;
        this.y = y;
        this.kalinlik = kalinlik;
    }

    public int[] getX() {
        return x;
    }

    public void setX(int[] x) {
        this.x = x;
    }

    public int[] getY() {
        return y;
    }

    public void setY(int[] y) {
        this.y = y;
    }

    public int getKalinlik() {
        return kalinlik;
    }

    public void setKalinlik(int kalinlik) {
        this.kalinlik = kalinlik;
    }

    
   
}
