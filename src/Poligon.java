
public class Poligon {
  private int[] x;
  private int[] y;
  private int tip;

    public Poligon(int[] x, int[] y, int tip) {
        this.x = x;
        this.y = y;
        this.tip = tip;
    }

    public int[] getX() {
        return x;
    }

    public void setX(int[] x) {
        this.x = x;
    }

    public int[] getY() {
        return y;
    }

    public void setY(int[] y) {
        this.y = y;
    }

    public int getTip() {
        return tip;
    }

    public void setTip(int tip) {
        this.tip = tip;
    }
  

  

}
